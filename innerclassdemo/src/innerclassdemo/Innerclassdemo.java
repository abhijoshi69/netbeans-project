package innerclassdemo;

public class Innerclassdemo 
{
    
   
    public static void main(String[] args) 
    {
        double d = 100.4;
        int i = (int)d;
        long l = (long)d;
        System.out.println("Double value is: "+d);
        System.out.println("Int value is: "+i);
        System.out.println("Long value is "+l);
      
        
    }
    
}
